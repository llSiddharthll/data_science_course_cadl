        // JavaScript for Timer
        const startingMinutes = 15;
        let time = startingMinutes * 60;
        const countdownElements = document.querySelectorAll('.bonus-expiry');
        const minutesElements = document.querySelectorAll('.minutes');
        const secondsElements = document.querySelectorAll('.seconds');

        function updateCountdown() {
            const minutes = Math.floor(time / 60);
            let seconds = time % 60;

            countdownElements.forEach(el => el.textContent = minutes);
            minutesElements.forEach(el => el.textContent = minutes);
            secondsElements.forEach(el => el.textContent = seconds < 10 ? '0' + seconds : seconds);

            if (time > 0) {
                time--;
            }
        }

